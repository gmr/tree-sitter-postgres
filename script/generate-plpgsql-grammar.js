#!/usr/bin/env node
'use strict';

/**
 * generate-plpgsql-grammar.js
 *
 * Generates plpgsql/grammar.js from PostgreSQL PL/pgSQL source.
 *
 * Unlike the postgres grammar (which auto-translates gram.y rules), the
 * PL/pgSQL grammar is hand-structured because pl_gram.y uses C action
 * code to consume SQL fragments at runtime (read_sql_expression, etc.).
 * Tree-sitter can't replicate that, so SQL expressions are captured as
 * opaque nodes and delegated to the postgres grammar via language injection.
 *
 * This generator:
 *   1. Reads the PL/pgSQL keyword lists to generate case-insensitive tokens
 *   2. Emits a hand-crafted grammar structure for the PL/pgSQL language
 *
 * Usage:
 *   node script/generate-plpgsql-grammar.js [postgres-dir]
 *
 * Default postgres-dir: ~/Source/gmr/postgres
 *
 * Output:
 *   plpgsql/grammar.js
 */

const path = require('path');
const fs = require('fs');
const os = require('os');

const { parsePlpgsqlKwlist } = require('./parse-plpgsql-kwlist');
const { caseInsensitiveRegex } = require('./parse-kwlist');

// ─── Paths ────────────────────────────────────────────────────────────────────

const postgresDir = process.argv[2] || path.join(os.homedir(), 'Source/gmr/postgres');
const projectRoot = path.join(__dirname, '..');

const reservedKwPath   = path.join(postgresDir, 'src/pl/plpgsql/src/pl_reserved_kwlist.h');
const unreservedKwPath = path.join(postgresDir, 'src/pl/plpgsql/src/pl_unreserved_kwlist.h');
const outputPath       = path.join(projectRoot, 'plpgsql/grammar.js');

// ─── Validate inputs ──────────────────────────────────────────────────────────

for (const p of [reservedKwPath, unreservedKwPath]) {
  if (!fs.existsSync(p)) {
    console.error(`ERROR: File not found: ${p}`);
    console.error(`       Pass the path to your postgres checkout as the first argument.`);
    process.exit(1);
  }
}

// ─── Parse keywords ───────────────────────────────────────────────────────────

console.log('Parsing PL/pgSQL keyword lists...');
const keywords = parsePlpgsqlKwlist(reservedKwPath, unreservedKwPath);
const reserved = keywords.filter(k => k.reserved);
const unreserved = keywords.filter(k => !k.reserved);
console.log(`  ${reserved.length} reserved keywords`);
console.log(`  ${unreserved.length} unreserved keywords`);

// ─── Keyword rule name ────────────────────────────────────────────────────────

function kwRuleName(token) {
  // K_ABORT -> kw_abort, K_ELSIF -> kw_elsif
  return `kw_${token.replace(/^K_/, '').toLowerCase()}`;
}

// ─── Generate keyword rules ──────────────────────────────────────────────────

function generateKeywordRules(keywords) {
  const lines = [];
  // Deduplicate: "elseif" and "elsif" both map to K_ELSIF
  const seen = new Set();
  for (const kw of keywords) {
    const ruleName = kwRuleName(kw.token);
    if (seen.has(ruleName)) continue;
    seen.add(ruleName);
    // Use the token name for the rule, but match any spelling
    // For K_ELSIF, we need to match both "elsif" and "elseif"
    const altNames = keywords.filter(k => k.token === kw.token).map(k => k.name);
    if (altNames.length === 1) {
      const regex = caseInsensitiveRegex(altNames[0]);
      lines.push(`    ${ruleName}: _ => token(prec(1, /${regex}/)),`);
    } else {
      // Multiple spellings for same token
      const patterns = altNames.map(n => `/${caseInsensitiveRegex(n)}/`);
      lines.push(`    ${ruleName}: _ => token(prec(1, choice(${patterns.join(', ')}))),`);
    }
  }
  return lines.join('\n');
}

// ─── Generate keyword category rules ─────────────────────────────────────────

function generateKwCategoryRule(name, kwList) {
  const seen = new Set();
  const choices = [];
  for (const kw of kwList) {
    const rn = kwRuleName(kw.token);
    if (seen.has(rn)) continue;
    seen.add(rn);
    choices.push(`$.${rn}`);
  }
  return `    ${name}: $ => choice(\n      ${choices.join(',\n      ')}\n    ),`;
}

// ─── Generate grammar ─────────────────────────────────────────────────────────

console.log('Generating plpgsql/grammar.js...');

const content = `// grammar.js — PL/pgSQL tree-sitter grammar
// Generated by script/generate-plpgsql-grammar.js from PostgreSQL source.
// DO NOT EDIT MANUALLY — regenerate with: npm run generate:plpgsql
//
// Source: src/pl/plpgsql/src/pl_gram.y + keyword lists
//
// PL/pgSQL's Bison grammar uses C action code to consume SQL expressions
// at runtime (read_sql_expression, read_sql_construct, etc.). Tree-sitter
// can't replicate that, so SQL fragments are captured as opaque nodes
// and delegated to the postgres grammar via tree-sitter language injection.

'use strict';

module.exports = grammar({
  name: 'plpgsql',

  extras: $ => [
    /\\s+/,
    $.comment,
  ],

  word: $ => $.identifier,

  externals: $ => [
    $._sql_body,
  ],

  conflicts: $ => [
    [$.raise_level, $.unreserved_keyword],
    [$.decl_statement, $.unreserved_keyword],
  ],

  rules: {

    // ── Entry point ───────────────────────────────────────────────────────────
    // A PL/pgSQL function body is: optional compiler options, a block, optional semicolon.
    source_file: $ => seq(
      optional($.comp_options),
      $.pl_block,
      optional(';')
    ),

    // ── Compiler options ──────────────────────────────────────────────────────
    // #option dump / #print_strict_params on|off / #variable_conflict ...
    comp_options: $ => repeat1($.comp_option),

    comp_option: $ => choice(
      seq('#', $.kw_option, $.kw_dump),
      seq('#', $.kw_print_strict_params, $.option_value),
      seq('#', $.kw_variable_conflict, $.kw_error),
      seq('#', $.kw_variable_conflict, $.kw_use_variable),
      seq('#', $.kw_variable_conflict, $.kw_use_column)
    ),

    option_value: $ => choice(
      $.identifier,
      $.unreserved_keyword
    ),

    // ── Block structure ───────────────────────────────────────────────────────
    pl_block: $ => seq(
      optional($.block_label),
      optional($.decl_sect),
      $.kw_begin,
      optional($.proc_sect),
      optional($.exception_sect),
      $.kw_end,
      optional($.end_label)
    ),

    block_label: $ => seq('<<', $.identifier, '>>'),

    end_label: $ => $.any_identifier,

    // ── Declaration section ───────────────────────────────────────────────────
    decl_sect: $ => seq(
      $.kw_declare,
      repeat($.decl_stmt)
    ),

    decl_stmt: $ => choice(
      $.decl_statement,
      $.kw_declare  // extra DECLARE is allowed
    ),

    decl_statement: $ => choice(
      // Variable declaration: name [CONSTANT] type [COLLATE collation] [NOT NULL] [DEFAULT|:=|= expr] ;
      seq(
        $.decl_varname,
        optional($.kw_constant),
        $.decl_datatype,
        optional($.decl_collate),
        optional(seq($.kw_not, $.kw_null)),
        optional($.decl_defval),
        ';'
      ),
      // Alias declaration: name ALIAS FOR target ;
      seq($.decl_varname, $.kw_alias, $.kw_for, $.any_identifier, ';'),
      // Cursor declaration: name [NO SCROLL | SCROLL] CURSOR [(args)] FOR|IS query ;
      seq(
        $.decl_varname,
        optional($.opt_scrollable),
        $.kw_cursor,
        optional($.decl_cursor_args),
        choice($.kw_is, $.kw_for),
        $.sql_expression,
        ';'
      )
    ),

    decl_varname: $ => choice(
      $.identifier,
      $.unreserved_keyword
    ),

    decl_datatype: $ => $.type_name,

    // Type names — simplified; covers most PL/pgSQL usage
    type_name: $ => choice(
      // variable%TYPE / table.column%TYPE
      seq($.dotted_name, '%', $.kw_type),
      // variable%ROWTYPE / table%ROWTYPE
      seq($.dotted_name, '%', $.kw_rowtype),
      // Regular type: name or schema.name, with optional array/precision modifiers
      seq(
        $.dotted_name,
        optional(seq('(', $.integer_literal, optional(seq(',', $.integer_literal)), ')')),
        repeat(seq('[', optional($.integer_literal), ']'))
      )
    ),

    dotted_name: $ => seq(
      $.any_identifier,
      repeat(seq('.', $.any_identifier))
    ),

    decl_collate: $ => seq($.kw_collate, $.dotted_name),

    decl_defval: $ => seq(
      choice('=', ':='),
      $.sql_expression
    ),

    opt_scrollable: $ => choice(
      seq($.kw_no, $.kw_scroll),
      $.kw_scroll
    ),

    decl_cursor_args: $ => seq(
      '(',
      $.decl_cursor_arg,
      repeat(seq(',', $.decl_cursor_arg)),
      ')'
    ),

    decl_cursor_arg: $ => seq($.decl_varname, $.decl_datatype),

    // ── Procedure section (statements) ────────────────────────────────────────
    proc_sect: $ => repeat1($.proc_stmt),

    proc_stmt: $ => choice(
      seq($.pl_block, ';'),
      $.stmt_assign,
      $.stmt_if,
      $.stmt_case,
      $.stmt_loop,
      $.stmt_while,
      $.stmt_for,
      $.stmt_foreach_a,
      $.stmt_exit,
      $.stmt_return,
      $.stmt_raise,
      $.stmt_assert,
      $.stmt_execsql,
      $.stmt_dynexecute,
      $.stmt_perform,
      $.stmt_call,
      $.stmt_getdiag,
      $.stmt_open,
      $.stmt_fetch,
      $.stmt_move,
      $.stmt_close,
      $.stmt_null,
      $.stmt_commit,
      $.stmt_rollback
    ),

    // ── Assignment ────────────────────────────────────────────────────────────
    // The target is consumed by the external scanner (it stops at := and =).
    // We use prec.dynamic to prefer assignments over exec SQL.
    stmt_assign: $ => prec.dynamic(10, seq(
      $.sql_expression,
      choice(':=', '='),
      $.sql_expression,
      ';'
    )),

    // ── IF/ELSIF/ELSE ─────────────────────────────────────────────────────────
    stmt_if: $ => seq(
      $.kw_if, $.sql_expression, $.kw_then,
      optional($.proc_sect),
      repeat($.elsif_clause),
      optional($.else_clause),
      $.kw_end, $.kw_if, ';'
    ),

    elsif_clause: $ => seq(
      $.kw_elsif, $.sql_expression, $.kw_then,
      optional($.proc_sect)
    ),

    else_clause: $ => seq(
      $.kw_else,
      optional($.proc_sect)
    ),

    // ── CASE ──────────────────────────────────────────────────────────────────
    stmt_case: $ => seq(
      $.kw_case,
      optional($.sql_expression),
      repeat1($.case_when),
      optional(seq($.kw_else, optional($.proc_sect))),
      $.kw_end, $.kw_case, ';'
    ),

    case_when: $ => seq(
      $.kw_when, $.sql_expression, $.kw_then,
      optional($.proc_sect)
    ),

    // ── LOOP ──────────────────────────────────────────────────────────────────
    stmt_loop: $ => seq(
      optional($.loop_label),
      $.kw_loop,
      $.loop_body
    ),

    // ── WHILE ─────────────────────────────────────────────────────────────────
    stmt_while: $ => seq(
      optional($.loop_label),
      $.kw_while, $.sql_expression, $.kw_loop,
      $.loop_body
    ),

    // ── FOR ───────────────────────────────────────────────────────────────────
    stmt_for: $ => seq(
      optional($.loop_label),
      $.kw_for, $.for_variable, $.kw_in,
      choice(
        $.for_integer_range,
        $.for_query,
        $.for_cursor,
        $.for_dynamic
      ),
      $.loop_body
    ),

    for_variable: $ => seq(
      $.any_identifier,
      repeat(seq(',', $.any_identifier))
    ),

    for_integer_range: $ => seq(
      optional($.kw_reverse),
      $.sql_expression, '..', $.sql_expression,
      optional(seq($.kw_by, $.sql_expression)),
      $.kw_loop
    ),

    for_query: $ => seq(
      $.sql_expression,
      $.kw_loop
    ),

    for_cursor: $ => seq(
      $.any_identifier,
      optional(seq('(', $.sql_expression, repeat(seq(',', $.sql_expression)), ')')),
      $.kw_loop
    ),

    for_dynamic: $ => seq(
      $.kw_execute, $.sql_expression,
      optional(seq($.kw_using, $.sql_expression, repeat(seq(',', $.sql_expression)))),
      $.kw_loop
    ),

    // ── FOREACH ───────────────────────────────────────────────────────────────
    stmt_foreach_a: $ => seq(
      optional($.loop_label),
      $.kw_foreach, $.for_variable,
      optional(seq($.kw_slice, $.integer_literal)),
      $.kw_in, $.kw_array,
      $.sql_expression, $.kw_loop,
      $.loop_body
    ),

    loop_label: $ => seq('<<', $.identifier, '>>'),
    loop_body: $ => seq(
      optional($.proc_sect),
      $.kw_end, $.kw_loop,
      optional($.any_identifier),
      ';'
    ),

    // ── EXIT/CONTINUE ─────────────────────────────────────────────────────────
    stmt_exit: $ => seq(
      choice($.kw_exit, $.kw_continue),
      optional($.any_identifier),
      optional(seq($.kw_when, $.sql_expression)),
      ';'
    ),

    // ── RETURN ────────────────────────────────────────────────────────────────
    stmt_return: $ => choice(
      // RETURN expression ;
      seq($.kw_return, optional($.sql_expression), ';'),
      // RETURN NEXT expression ;
      seq($.kw_return, $.kw_next, $.sql_expression, ';'),
      // RETURN QUERY sql ;
      seq($.kw_return, $.kw_query, $.sql_expression, ';'),
      // RETURN QUERY EXECUTE expression [USING ...] ;
      seq($.kw_return, $.kw_query, $.kw_execute, $.sql_expression,
          optional(seq($.kw_using, $.sql_expression, repeat(seq(',', $.sql_expression)))),
          ';')
    ),

    // ── RAISE ─────────────────────────────────────────────────────────────────
    stmt_raise: $ => seq(
      $.kw_raise,
      optional($.raise_level),
      optional(choice(
        // Format string with parameters
        seq($.string_literal, repeat(seq(',', $.sql_expression))),
        // Condition name
        $.any_identifier,
        // SQLSTATE 'xxxxx'
        seq($.kw_sqlstate, $.string_literal)
      )),
      optional(seq($.kw_using, $.raise_option, repeat(seq(',', $.raise_option)))),
      ';'
    ),

    raise_level: $ => choice(
      $.kw_exception,
      $.kw_warning,
      $.kw_notice,
      $.kw_info,
      $.kw_log,
      $.kw_debug
    ),

    raise_option: $ => seq(
      choice(
        $.kw_message,
        $.kw_detail,
        $.kw_hint,
        $.kw_errcode,
        $.kw_column,
        $.kw_constraint,
        $.kw_datatype,
        $.kw_table,
        $.kw_schema
      ),
      '=',
      $.sql_expression
    ),

    // ── ASSERT ────────────────────────────────────────────────────────────────
    stmt_assert: $ => seq(
      $.kw_assert,
      $.sql_expression,
      optional(seq(',', $.sql_expression)),
      ';'
    ),

    // ── EXECUTE (dynamic SQL) ─────────────────────────────────────────────────
    stmt_dynexecute: $ => seq(
      $.kw_execute, $.sql_expression,
      optional(seq($.kw_into, optional($.kw_strict), $.into_target)),
      optional(seq($.kw_using, $.sql_expression, repeat(seq(',', $.sql_expression)))),
      ';'
    ),

    // ── PERFORM ───────────────────────────────────────────────────────────────
    stmt_perform: $ => seq(
      $.kw_perform, $.sql_expression, ';'
    ),

    // ── CALL / DO ─────────────────────────────────────────────────────────────
    stmt_call: $ => seq(
      choice($.kw_call, $.kw_do), $.sql_expression, ';'
    ),

    // ── GET DIAGNOSTICS ───────────────────────────────────────────────────────
    stmt_getdiag: $ => seq(
      $.kw_get,
      optional(choice($.kw_current, $.kw_stacked)),
      $.kw_diagnostics,
      $.getdiag_list_item,
      repeat(seq(',', $.getdiag_list_item)),
      ';'
    ),

    getdiag_list_item: $ => seq(
      $.any_identifier,
      choice('=', ':='),
      $.getdiag_item
    ),

    getdiag_item: $ => choice(
      $.kw_row_count,
      $.kw_pg_routine_oid,
      $.kw_pg_context,
      $.kw_pg_exception_detail,
      $.kw_pg_exception_hint,
      $.kw_pg_exception_context,
      $.kw_column_name,
      $.kw_constraint_name,
      $.kw_pg_datatype_name,
      $.kw_message_text,
      $.kw_table_name,
      $.kw_schema_name,
      $.kw_returned_sqlstate
    ),

    // ── OPEN cursor ───────────────────────────────────────────────────────────
    stmt_open: $ => seq(
      $.kw_open,
      $.any_identifier,
      optional(choice(
        // Unbound cursor: OPEN cur [NO SCROLL | SCROLL] FOR query|EXECUTE expr
        seq(
          optional($.opt_scrollable),
          $.kw_for,
          choice(
            seq($.kw_execute, $.sql_expression,
                optional(seq($.kw_using, $.sql_expression, repeat(seq(',', $.sql_expression))))),
            $.sql_expression
          )
        ),
        // Bound cursor: OPEN cur (args)
        seq('(', $.sql_expression, repeat(seq(',', $.sql_expression)), ')')
      )),
      ';'
    ),

    // ── FETCH ─────────────────────────────────────────────────────────────────
    stmt_fetch: $ => seq(
      $.kw_fetch,
      optional($.fetch_direction),
      optional(seq($.kw_from, $.any_identifier)),
      $.kw_into,
      $.into_target,
      ';'
    ),

    // ── MOVE ──────────────────────────────────────────────────────────────────
    stmt_move: $ => seq(
      $.kw_move,
      optional($.fetch_direction),
      $.any_identifier,
      ';'
    ),

    fetch_direction: $ => choice(
      $.kw_next,
      $.kw_prior,
      $.kw_first,
      $.kw_last,
      seq($.kw_absolute, $.sql_expression),
      seq($.kw_relative, $.sql_expression),
      $.kw_forward,
      $.kw_backward,
      seq($.kw_forward, choice($.integer_literal, $.kw_all)),
      seq($.kw_backward, choice($.integer_literal, $.kw_all))
    ),

    // ── CLOSE ─────────────────────────────────────────────────────────────────
    stmt_close: $ => seq($.kw_close, $.any_identifier, ';'),

    // ── NULL statement ────────────────────────────────────────────────────────
    stmt_null: $ => seq($.kw_null, ';'),

    // ── COMMIT/ROLLBACK ───────────────────────────────────────────────────────
    stmt_commit: $ => seq(
      $.kw_commit,
      optional($.opt_transaction_chain),
      ';'
    ),

    stmt_rollback: $ => seq(
      $.kw_rollback,
      optional($.opt_transaction_chain),
      ';'
    ),

    opt_transaction_chain: $ => choice(
      seq($.kw_and, $.kw_chain),
      seq($.kw_and, $.kw_no, $.kw_chain)
    ),

    // ── Exec SQL (pass-through SQL statement) ─────────────────────────────────
    stmt_execsql: $ => seq(
      $.sql_expression,
      optional(seq($.kw_into, optional($.kw_strict), $.into_target)),
      ';'
    ),

    into_target: $ => seq(
      $.any_identifier,
      repeat(seq(',', $.any_identifier))
    ),

    // ── Exception handling ────────────────────────────────────────────────────
    exception_sect: $ => seq(
      $.kw_exception,
      repeat1($.proc_exception)
    ),

    proc_exception: $ => seq(
      $.kw_when,
      $.proc_conditions,
      $.kw_then,
      optional($.proc_sect)
    ),

    proc_conditions: $ => seq(
      $.proc_condition,
      repeat(seq($.kw_or, $.proc_condition))
    ),

    proc_condition: $ => choice(
      $.any_identifier,
      seq($.kw_sqlstate, $.string_literal)
    ),

    // ── SQL expression ────────────────────────────────────────────────────────
    // Opaque node that captures a SQL expression fragment. The external
    // scanner (src/scanner.c) consumes tokens until a PL/pgSQL structural
    // delimiter is found (THEN, LOOP, END, ;, etc.), balancing parentheses
    // and string literals. The actual SQL parsing is delegated to the
    // postgres grammar via tree-sitter language injection.
    sql_expression: $ => $._sql_body,

    // ── Common helpers ────────────────────────────────────────────────────────
    any_identifier: $ => choice(
      $.identifier,
      $.unreserved_keyword
    ),

    // ── Keyword category lists ────────────────────────────────────────────────

${generateKwCategoryRule('reserved_keyword', reserved)}

${generateKwCategoryRule('unreserved_keyword', unreserved)}

    // ── Keyword tokens (case-insensitive) ─────────────────────────────────────

${generateKeywordRules(keywords)}

    // ── Lexer rules ───────────────────────────────────────────────────────────
    // Only tokens referenced by grammar rules are included here.
    // SQL fragment tokens (float, escape strings, etc.) are handled by the
    // external scanner and parsed via injection into the postgres grammar.

    identifier: _ => token(prec(0, /[a-zA-Z_\\u0080-\\u00ff][a-zA-Z0-9_$\\u0080-\\u00ff]*/)),

    integer_literal: _ => token(/[0-9](_?[0-9])*/),

    string_literal: _ => token(/'([^']|'')*'/),

    comment: _ => token(choice(
      /--[^\\r\\n]*/,
      /\\/\\*[^*]*\\*+([^/*][^*]*\\*+)*\\//
    )),

  }, // end rules
}); // end grammar
`;

fs.writeFileSync(outputPath, content, 'utf8');

const lineCount = content.split('\n').length;
const byteCount = Buffer.byteLength(content, 'utf8');
console.log(`  Wrote ${outputPath}`);
console.log(`  ${lineCount} lines, ${Math.round(byteCount / 1024)} KB`);
console.log('');
console.log('Next step: cd plpgsql && tree-sitter generate');
